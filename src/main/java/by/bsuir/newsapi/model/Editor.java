package by.bsuir.newsapi.model;

import java.util.Objects;

public class Editor {
    private long id;

    private String login;

    private String password;

    private String firstName;

    private String lastName;

    public Editor() {
    }

    public Editor(long id, String login, String password, String firstName, String lastName) {
        this.id = id;
        this.login = login;
        this.password = password;
        this.firstName = firstName;
        this.lastName = lastName;
    }

    public Editor(String login, String password, String firstName, String lastName) {
        this.login = login;
        this.password = password;
        this.firstName = firstName;
        this.lastName = lastName;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Editor editor = (Editor) o;
        return id == editor.id && login.equals(editor.login) && password.equals(editor.password) && firstName.equals(editor.firstName) && lastName.equals(editor.lastName);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, login, password, firstName, lastName);
    }
}
